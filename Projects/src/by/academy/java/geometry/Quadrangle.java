package by.academy.java.geometry;

public abstract class Quadrangle extends Figure{  // Четырехугольник

    public Quadrangle(double height) {
        super(height);
    }

    @Override
    public Double getArea() {
        return super.getArea();
    }

    @Override
    public void isEqual(Double figureArea) {
        super.isEqual(figureArea);
    }
}
